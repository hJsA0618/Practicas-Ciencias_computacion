#include <stdio.h>

int main() {
    double e = 1.0; 
    int iteracion = 0;
    while (1 + (e * 0.5) > 1) { 
        /*comparado con 1 porque Al comparar contra 0, los números de punto flotante muy pequeños se pueden 
        tratar como 0 debido a su precisión limitada.*/ 
        ++iteracion; 
        e *= 0.5;
        printf("Iteracion %d: x = %G\n", iteracion, e);
    }
   // printf("Epsilon de la maquina en forma binaria = 2^(-%d)\n", n);
    printf("Epsilon de la maquina en forma decimal = %g\n", e);//% %G para notacion cientifica
return 0;
}
