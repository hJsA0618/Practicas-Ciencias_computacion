#include <stdio.h>
#include <math.h>

int main(void) {
    int numero=65530; //valor antes de que se desborde la variable
    double pi=0, sumatoria = 0.0;  

    if (numero <= 0) {
        printf("Por favor, ingresa un número entero positivo mayor que 0.\n");
        return 1;
    }

    for (int n = 1; n <= numero; n++) {
        sumatoria += 1.0 / (n * n); 
        pi= sqrt(sumatoria*6);
    }

    //printf("La sumatoria de 1/(n^2) para n de 1 a %d es %f\n", numero, sumatoria);
    printf("pi es igual a %.7f", pi);

    return 0;
}
